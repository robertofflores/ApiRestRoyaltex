package com.tech.ws.rest.vo;

public class VOSucursal {
	private int codigo;
	private String nombre;
	private int habilitar;
	private int numdiasncre;
	
	public int getNumdiasncre() {
		return numdiasncre;
	}
	public void setNumdiasncre(int numdiasncre) {
		this.numdiasncre = numdiasncre;
	}
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getHabilitar() {
		return habilitar;
	}
	public void setHabilitar(int habilitar) {
		this.habilitar = habilitar;
	}
	
	
}
